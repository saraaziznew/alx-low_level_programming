#include "3-calc.h"
/**
 * op_add - adda 2 integer
 * @a:string to print
 * @b:string to print
 * Return: 0 (success)
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts 2 integer
 * @a: string to print 
 * @b: string to print 
 * Return: 0 (success)
 */
int op_sub(int a, int b)
{
        return (a - b);
}
/**
 * op_mul - multiple 2 integer
 * @a: string to print
 * @b: string to print
 * Return: 0 (success)
 */
int op_mul(int a, int b)
{
        return (a * b);
}
/**
 * op_div - divided 2 integer
 * @a: string to print
 * @b: string to print
 * Return: 0 (success)
 */
int op_div(int a, int b)
{
        return (a / b);
}
/**
 * op_mod - modulo 2 integer
 * @a: string to print
 * @b: string to print
 * Return: 0 (success)
 */
int op_mod(int a, int b)
{
        return (a % b);
}
